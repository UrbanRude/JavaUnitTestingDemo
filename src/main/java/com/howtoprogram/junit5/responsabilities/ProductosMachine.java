package com.howtoprogram.junit5.responsabilities;

public class ProductosMachine {
	
	public int gansitos;
	public int nitos;
	public int paletas;
	private double money;
	
	private String tipo;
	
	public double getMoney() {
		return money;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String tipo) {
		this.tipo = tipo;
	}
	public void setMoney(double money) {
		this.money = money;
	}
	public int getGansitos() {
		return gansitos;
	}
	public void setGansitos(int gansitos) {
		this.gansitos = gansitos;
	}
	public int getNitos() {
		return nitos;
	}
	public void setNitos(int nitos) {
		this.nitos = nitos;
	}
	public int getPaletas() {
		return paletas;
	}
	public void setPaletas(int paletas) {
		this.paletas = paletas;
	}	
	
	
}
